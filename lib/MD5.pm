package MD5;
use Moo;
extends 'Roll';

##-----------------------------------------------------------
##---------------------- ATTRIBUTES -------------------------
##-----------------------------------------------------------

##-----------------------------------------------------------
##------------------------ METHODS --------------------------
##-----------------------------------------------------------

sub MD5_checksum {
    my $tape = shift;
    $tape->pull;

    my $opts = $tape->options;

    my @cmds;
    while ( my $file = $tape->next ) {
        chomp $file;
        next unless ( $file =~ /md5_checksums.txt/ );

        my $cmd = sprintf(
            "md5sum -c %s", $file
        );
        push @cmds, $cmd;
    }
    $tape->bundle(\@cmds);
}

##-----------------------------------------------------------

1;

